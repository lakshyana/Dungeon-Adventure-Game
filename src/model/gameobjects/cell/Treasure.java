package model.gameobjects.cell;

/**
 * This class represents the different types of treasure that may be present in the caves of a
 * dungeon. The three types of treasure found there are : diamonds, rubies, and sapphires. A random
 * treasure of at least 20% is made available in the caves of the dungeon.
 */
public enum Treasure {
  DIAMONDS, RUBIES, SAPPHIRES
}
